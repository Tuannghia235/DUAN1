CREATE TABLE THANHTOAN(
	ID INT PRIMARY KEY IDENTITY,
	PHUONGTHUC INT
);

CREATE TABLE SANPHAM(
	ID INT PRIMARY KEY IDENTITY,
	MASP VARCHAR(05),
	TEN NVARCHAR(50),
	TRANGTHAI BIT,
);

CREATE TABLE THUONGHIEU(
	ID INT PRIMARY KEY IDENTITY,
	MA VARCHAR(05),
	TEN NVARCHAR(20),
	NGUONGOC NVARCHAR(20)
);

CREATE TABLE KIEUDANG(
	ID INT PRIMARY KEY IDENTITY,
	MA VARCHAR(05),
	TEN NVARCHAR(20)
);

CREATE TABLE LOAI(
	ID INT PRIMARY KEY IDENTITY,
	TEN NVARCHAR(20)
);

CREATE TABLE KICHCO(
	ID INT PRIMARY KEY IDENTITY,
	MA VARCHAR(05),
	Inch INT
);

CREATE TABLE MANHINH(
	ID INT PRIMARY KEY IDENTITY,
	MA VARCHAR(05),
	TEN NVARCHAR(20)
);

CREATE TABLE DOPHANGIAI(
	ID INT PRIMARY KEY IDENTITY,
	MA VARCHAR(05),
	TEN NVARCHAR(20)
);

CREATE TABLE SANPHAMCHITIET(
	ID INT PRIMARY KEY IDENTITY,
	MASPCT VARCHAR(05),
	ID_SP INT FOREIGN KEY REFERENCES SANPHAM(ID),
	ID_THUONGHIEU INT FOREIGN KEY REFERENCES THUONGHIEU(ID),
	ID_KIEUDANG INT FOREIGN KEY REFERENCES KIEUDANG(ID),
	ID_LOAI INT FOREIGN KEY REFERENCES LOAI(ID),
	ID_KICHCO INT FOREIGN KEY REFERENCES KICHCO(ID),
	ID_MANHINH INT FOREIGN KEY REFERENCES MANHINH(ID),
	ID_PHANGIAI INT FOREIGN KEY REFERENCES DOPHANGIAI(ID),
	SOLUONG INT,
	DONGIA MONEY,
	TRANGTHAI BIT
);
CREATE TABLE HOADON(
	ID INT PRIMARY KEY IDENTITY,
	MAHD VARCHAR(05),
	ID_KH INT FOREIGN KEY REFERENCES KHACHHANG(ID),
	ID_NV INT FOREIGN KEY REFERENCES NHANVIEN(ID),
	ID_THANHTOAN INT FOREIGN KEY REFERENCES THANHTOAN(ID),
	TEN NVARCHAR(20),
	SODIENTHOAI VARCHAR(10),
	NGAY DATE,
	TONGTIEN INT,
	TRANGTHAI INT
);
CREATE TABLE HOADONCHITIET(
	ID INT PRIMARY KEY IDENTITY,
	MAHDCT VARCHAR(05),
	ID_HD INT FOREIGN KEY REFERENCES HOADON(ID),
	ID_SPCT INT FOREIGN KEY REFERENCES SANPHAMCHITIET(ID),
	SOLUONG INT,
	DONGIA MONEY
);
CREATE TABLE PHIEUGIAMGIA(
	MAPHIEU VARCHAR(05) PRIMARY KEY,
	ID_HD INT FOREIGN KEY REFERENCES HOADON(ID),
	PHANTRAM INT,
	NGAYTAO DATE,
	NGAYHETHAN DATE,
	TRANGTHAI INT
);

