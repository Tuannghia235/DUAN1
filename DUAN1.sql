CREATE TABLE KHACHHANG(
	ID INT PRIMARY KEY IDENTITY,
	MAKH VARCHAR(05),
	TEN NVARCHAR(20),
	SODIENTHOAI VARCHAR(10),
	DIACHI NVARCHAR(30),
	GIOITINH INT,
	NGAYSINH DATE
);

CREATE TABLE NHANVIEN(
	ID INT PRIMARY KEY IDENTITY,
	MANV VARCHAR(05),
	TEN NVARCHAR(20),
	SODIENTHOAI VARCHAR(10),
	EMAIL VARCHAR(30),
	PASS NVARCHAR(30),
	GIOITINH INT,
	CHUCVU INT,
	TRANGTHAI BIT
