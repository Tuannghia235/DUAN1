    public  void loadData(ArrayList<phieuGiamGia> inputlist) throws SQLException{ 
        list = inputlist;
        this.fillToTable();
    }
    
    public void fillToTable() {
    DefaultTableModel model = (DefaultTableModel) tbl_GiamGia.getModel();
    model.setRowCount(0);
    for (phieuGiamGia phieu : list) {
        Object[] row = {
            phieu.getMaPhieu(),
            phieu.getIdHd(),
            phieu.getPhanTram(),
            phieu.getNgayTao(),
            phieu.getNgayHetHan(),
            phieu.getTrangThai() == 3 ? "Đã bị hủy" : phieu.getTrangThai() == 2 ? "Đã kết thúc" : phieu.getTrangThai() == 1 ? "Đang diễn ra" : "Sắp diễn ra"
        };
        model.addRow(row);
    }
}
    
    public String getOrder(){
        String Order = "MAPHIEU";
        switch (cbo_SapXep.getSelectedItem().toString()) {
            case "Mã":
                Order = "MAPHIEU";
                break;
            case "Loại":
                Order = "PHANTRAM";
                break;
            case "Ngày bắt đầu":
                Order = "NGAYTAO";
                break;
            case "Ngày kết thúc":
                Order = "NGAYKETTHUC";
                break;
            case "Trạng thái":
                Order = "TRANGTHAI";
                break;
        }
        
        return Order;
    }
    
    public boolean checkMa() throws SQLException{
        list = repo.getAll(getOrder());
        boolean check = false;
        for (phieuGiamGia phieu : list) {
            if(this.readForm().getMaPhieu().equalsIgnoreCase(phieu.getMaPhieu())){
                check = true;
            }
        }   
        return check;
    }

