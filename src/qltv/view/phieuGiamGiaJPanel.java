    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (checkMa()== true) {
                JOptionPane.showMessageDialog(this, "Bạn đã nhập trùng mã");
            } else{
                if (txt_Ma.getText().trim().equals("")) {
                    JOptionPane.showMessageDialog(this, "Mã không được để trống");
                    }else if (txt_HoaDon.getText().trim().equals("")) {
                       JOptionPane.showMessageDialog(this, "Tên không được để trống");
        } else  if(repo.them(this.readForm()) > 0){
                repo.updateTrangThai();
                this.loadData(repo.getAll(getOrder()));
                JOptionPane.showMessageDialog(this,"Thêm thành công");
            }
            else    JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
        } catch (SQLException ex) {
            Logger.getLogger(khachHangJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

