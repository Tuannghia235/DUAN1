    private void btn_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimKiemActionPerformed
     String input = txt_TimKiem.getText().toString().trim();
     int trangThai = -1;
        switch (cbo_Loc.getSelectedItem().toString()) {
            case "Tất cả":
                break;
            case "Đang diễn ra":
                trangThai = 1;
                break;             
            case "Sắp diễn ra":
                trangThai = 0;
                
                break;  
            case "Đã kết thúc":
                trangThai = 2;
                break;  
            case "Đã bị hủy":
                trangThai = 3;
                break;     
        }
        if (!input.isEmpty()) {
            try {
                if (!repo.timKiem(trangThai ,input, getOrder()).isEmpty()) {
                    JOptionPane.showMessageDialog(this, "tìm thấy");
                   
                    loadData(repo.timKiem(trangThai,input, getOrder()));
                } else {
                    JOptionPane.showMessageDialog(this, "ko tìm thấy");
                }
            } catch (SQLException ex) {
                Logger.getLogger(khachHangJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else
            JOptionPane.showMessageDialog(this, "ô tìm kiếm đang trống");
    
    }//GEN-LAST:event_btn_TimKiemActionPerformed

